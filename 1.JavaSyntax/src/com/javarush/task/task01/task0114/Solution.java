/* Теперь я буду гнуть что хочу
Напиши программу, которая выводит на экран надпись: «Теперь я буду гнуть что хочу, когда хочу, кого хочу!» 4 раза.
*/
public class Solution
{
    public static void main(String[] args)
    {
        for (int i = 0; i < 4; i++)
        {
            System.out.println("Теперь я буду гнуть что хочу, когда хочу, кого хочу!");
        }
    }
}